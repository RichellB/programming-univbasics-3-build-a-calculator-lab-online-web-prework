def addition(num1, num2)
  sum = num1 + num2
  sum
end


def subtraction(num1, num2)
  diff = num1 - num2
  diff

end


def division(num1, num2)
  div = num2 / num1
  div

end


def multiplication(num1, num2)
  quotient = num1 * num2
  quotient

end


def modulo(num1, num2)
  div_mod = num2 % num1
  div_mod

end


def square_root(num)
  sqr_rt = num.Math.sqrt
  sqr_rt

end
